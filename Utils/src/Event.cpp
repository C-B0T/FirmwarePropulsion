namespace Utils
{
	Event::Event()
	{
	}

	Event& Event::operator += (EventCallback cb)
	{
		this->Subscribe(cb);

		return *this;
	}

	Event& Event::operator -= (EventCallback cb)
	{
		this->Unsubscribe(cb);

		return *this;
	}
}
